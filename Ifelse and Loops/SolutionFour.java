package com.gfg.basic.java;

public class SolutionFour {
	public static void main(String[] args) {
//		Java Program to Swap Two Numbers
		int temp;
		int a = 10;
		int b = 20;
		
		System.out.println("A: "+a);
		System.out.println("B: "+b);
		
		temp = a;
		a = b;
		b = temp;
		
		System.out.println("A: "+a);
		System.out.println("B: "+b);
		
		
		
	}
}
